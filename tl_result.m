function [row] = tl_result( ...
    transformation_scale, ...
    transformation_rotate, ...
    s, ...
    theta ...
    )
row = [];
row(1) = transformation_scale;
row(2) = transformation_rotate;
row(3) = s;
row(4) = theta;
row(5) = abs(transformation_scale - s);
row(6) = abs(transformation_rotate - theta);
end